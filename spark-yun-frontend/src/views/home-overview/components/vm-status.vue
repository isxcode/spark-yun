<template>
  <div :class="vmStatusClass">
    <div class="vm-status__pointer"></div>
    <span class="vm-status__text">{{ vmStatus.name }}</span>
  </div>
</template>

<script setup lang="ts">
import { computed } from 'vue';
import { VmData } from './component'

const props = withDefaults(defineProps<{
  status: VmData['status']
}>(), {
  status: 'SUCCESS'
})

const vmStatusClass = computed(() => {
  return [
    'vm-status',
    'is-' + props.status.toLowerCase()
  ]
})

const vmStatus = computed<{ status: VmData['status'], name: string }>(() => {
  return {
    status: props.status,
    name: props.status === 'FAILED' ? '失败' : '成功'
  }
})

</script>

<style scoped lang="scss">
.vm-status {
  display: flex;
  align-items: center;
  justify-content: center;
  font-size: 12px;
  line-height: 22px;
  &.is-success {
    color: #43CF7C;
    .vm-status__pointer {
      background-color: #43CF7C;
    }
  }
  &.is-failed {
    color: #FA541C;
    .vm-status__pointer {
      background-color: #FA541C;
    }
  }

  &__pointer {
    width: 8px;
    height: 8px;
    border-radius: 50%;
    margin-right: 4px;
  }
}
</style>